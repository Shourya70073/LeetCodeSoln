class Solution
{
public:
   pair<int,int> kaamkaro(Node *root){
       if(root==nullptr){
           pair<int,int>p;
           p.first=0;
           p.second=0;
           return p;
       }
       pair<int,int>p1=kaamkaro(root->left);
       pair<int,int>p2=kaamkaro(root->right);
       int sum=root->data;
       if(p1.second>p2.second){
            return make_pair(p1.first+sum, p1.second+1);
        }else if(p1.second<p2.second){
            return make_pair(p2.first+sum, p2.second+1);
        }else{
            return (p1.first>=p2.first)?make_pair(p1.first+sum, p1.second+1):
            make_pair(p2.first+sum, p2.second+1);
        }
       
   }
    
    int sumOfLongRootToLeafPath(Node *root)
    {
       pair<int,int>p=kaamkaro(root);
       return p.first;
    }
};

